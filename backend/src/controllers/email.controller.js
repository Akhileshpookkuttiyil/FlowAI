import { config } from "../config/env.js";
import { asyncHandler } from "../utils/asyncHandler.js";
import { sendEmail as brevoEmailAction } from "../services/emailService.js";
import { Response } from "../models/response.model.js";
import { clerkClient } from "@clerk/clerk-sdk-node";

export const sendEmail = asyncHandler(async (req, res) => {
  const { responseId } = req.body;
  const userId = req.userId;

  if (!config.brevoApiKey || !config.brevoSenderEmail) {
    return res.status(500).json({
      success: false,
      error: "Brevo email is not configured. Set BREVO_API_KEY and BREVO_SENDER_EMAIL.",
    });
  }

  const aiResponse = await Response.findById(responseId);
  if (!aiResponse) {
    return res.status(404).json({ success: false, error: "Response not found." });
  }

  if (aiResponse.userId !== userId) {
    return res.status(403).json({ success: false, error: "Unauthorized access to this response." });
  }

  const user = await clerkClient.users.getUser(userId);
  if (!user) {
     return res.status(404).json({ success: false, error: "User not found." });
  }

  const primaryEmailId = user.primaryEmailAddressId;
  const emailObj = user.emailAddresses.find(e => e.id === primaryEmailId) || user.emailAddresses[0];
  if (!emailObj || !emailObj.emailAddress) {
     return res.status(403).json({ success: false, error: "No verified email address found for your account." });
  }
  const toEmail = emailObj.emailAddress;

  const subject = "Your FlowAI Response";
  const message = `Here is your AI response from FlowAI:\n\n${aiResponse.response}\n\n---\nPrompt Reference: ${aiResponse.prompt}\nGenerated at: ${new Date().toISOString()}\n\nThis email was generated by FlowAI based on your request.`;

  try {
    const brevoResult = await brevoEmailAction({ to: toEmail, subject, message });
    console.log(`[Email Success] userId: ${userId}, responseId: ${responseId}, timestamp: ${new Date().toISOString()}`);
    return res.status(200).json({
      success: true,
      provider: "brevo",
      message: "Email accepted by Brevo for delivery.",
      messageId: brevoResult?.messageId || brevoResult?.messageIds?.[0] || null,
    });
  } catch (error) {
    console.error(`[Email Failed] userId: ${userId}, responseId: ${responseId}, timestamp: ${new Date().toISOString()} - Error:`, error.message);
    return res.status(error.status || 502).json({
      success: false,
      error: error.message || "Failed to send email via Brevo API.",
    });
  }
});
